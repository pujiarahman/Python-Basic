class berhitung:
    def tambah(a,b):
        x = a+b
        print(x)
    def kurang(a,b):
        x = a-b
        print(x)
    def bagi(a,b):
        x = a/b
        print(x)
    def kali(a,b):
        x = a * b
        print(x)

berhitung.tambah(2,2)
berhitung.kurang(2,2)
berhitung.bagi(2,2)
berhitung.kali(2,2)
    
